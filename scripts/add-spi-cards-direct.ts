async function main() {
  // 1. SPIデッキを探す or 作成
  const { data: existingDecks } = await supabase
    .from("decks")
    .select("*")
    .eq("user_id", USER_ID)
    .eq("name", "SPI");

  let spiDeck = existingDecks?.[0];

  if (!spiDeck) {
    console.log("📚 SPIデッキが見つかりません。作成します...");
    const { data: newDeck, error: createError } = await supabase
      .from("decks")
      .insert({
        user_id: USER_ID,
        name: "SPI",
        description: "SPI対策問題集",
        sort_order: 0,
      })
      .select()
      .single();

    if (createError) {
      console.error("❌ デッキ作成エラー:", createError.message);
      process.exit(1);
    }
    spiDeck = newDeck;
    console.log("✅ SPIデッキ作成完了:", spiDeck.id);
  } else {
    console.log("✅ SPIデッキ見つかりました:", spiDeck.id);
  }

  // 2. カードを一括追加
  console.log(`📝 ${cards.length}枚のカードを追加中...`);
  const insertData = cards.map((card) => ({
    deck_id: spiDeck.id,
    user_id: USER_ID,
    front: card.front.trim(),
    back: card.back.trim(),
    tags: card.tags,
    card_type: "basic" as const,
  }));

  const { data, error } = await supabase
    .from("cards")
    .insert(insertData)
    .select();

  if (error) {
    console.error("❌ カード追加エラー:", error.message);
    process.exit(1);
  }

  console.log(`✅ ${data.length}枚のカードを追加しました！`);
  console.log("🎯 SPIデッキID:", spiDeck.id);
}

main();

export function isIconItem(item: { name: string }): boolean {
  return item.name.startsWith("【アイコン】");
}

export function isRefinedItem(item: { name: string }): boolean {
  return item.name.startsWith("精錬:") || item.name.startsWith("邊ｾ骭ｬ:");
}

export function itemDisplayName(itemOrName: { name: string } | string): string {
  const name = typeof itemOrName === "string" ? itemOrName : itemOrName.name;
  return name.replace("精錬:", "").replace("邊ｾ骭ｬ:", "");
}

export function refinedPartRarity(part: string | null, items: any[]): number {
  if (!part) return 1;
  let value = 1;
  for (const item of items) {
    if (item?.name?.includes(part)) {
      value = Math.max(value, RARITY_ORDER[item.rarity] || 1);
    }
  }
  return value;
}
